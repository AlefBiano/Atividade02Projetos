
public abstract class caixaAbstrato {

    public caixaAbstrato(int cem, int cinquenta, int vinte, int cinco) {
        this.cem = cem;
        this.cinquenta = cinquenta;
        this.vinte = vinte;
        this.cinco = cinco;
    }
    protected int cem;
    protected int cinquenta;
    protected int vinte;
    protected int cinco;
}
